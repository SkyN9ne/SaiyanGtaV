typedef struct _Vector3
{
	float x, y, z;
	_Vector3()
	{
		x = y = z = 0.0;
	}
	_Vector3(float x, float y, float z)
	{
		this->x = x, this->y = y, this->z = z;
	}
	bool operator==(_Vector3 Vector)
	{
		return (this->x == Vector.x && this->y == Vector.y && this->z == Vector.z);
	}
	bool operator!=(_Vector3 Vector)
	{
		return (this->x != Vector.x || this->y != Vector.y || this->z != Vector.z);
	}
	_Vector3 operator+(_Vector3 Vector)
	{
		return _Vector3(this->x + Vector.x, this->y + Vector.y, this->z + Vector.z);
	}
	void operator+=(_Vector3 Vector)
	{
		this->x += Vector.x;
		this->y += Vector.y;
		this->z += Vector.z;
	}
	_Vector3 operator-(_Vector3 Vector)
	{
		return _Vector3(this->x - Vector.x, this->y - Vector.y, this->z - Vector.z);
	}
	void operator-=(_Vector3 Vector)
	{
		this->x -= Vector.x;
		this->y -= Vector.y;
		this->z -= Vector.z;
	}
	_Vector3 operator*(float scale) const
	{
		return _Vector3(x * scale, y * scale, z * scale);
	}
	float Distance()
	{
		return sqrt(DistanceEx(_Vector3(x, y, z)));
	}
	float Distance(_Vector3 Vector)
	{
		return sqrt(DistanceEx(Vector));
	}
	float DistanceEx(_Vector3 Vector)
	{
		float _x = this->x - Vector.x, _y = this->y - Vector.y, _z = this->z - Vector.z;
		return ((_x * _x) + (_y * _y) + (_z * _z));
	}
	float DistanceTo(_Vector3 Vector)
	{
		_Vector3 ret = _Vector3(x, y, z) - Vector;
		return ret.Distance();
	}
	float DotProduct(_Vector3 Vector)
	{
		return (this->x * Vector.x) + (this->y * Vector.y) + (this->z * Vector.z);
	}
	bool isNull()
	{
		return (this->x == 0.0 && this->y == 0.0 && this->z == 0.0);
	}
	void Normalize()
	{
		float len = Distance();
		if (len != 0.0)
		{
			float num = (float)(1.0 / ((double)len));
			this->x *= num;
			this->y *= num;
			this->z *= num;
		}
	}
	_Vector3 Normalize(_Vector3 Vector)
	{
		float len = Vector.Distance();
		if (len != 0.0)
		{
			float num = (float)(1.0 / ((double)len));
			return _Vector3(Vector.x * num, Vector.y * num, Vector.z * num);
		}
	}
	_Vector3 WorldUp()
	{
		return _Vector3(0.0, 0.0, 1.0);
	}
	_Vector3 Zero()
	{
		return _Vector3(0, 0, 0);
	}
} Vector3, *pVector3;

